def cycleToChromosome(cycle):
    M = max(cycle)
    m = min(cycle)
    chr = [i for i in range(int((m+1)/2), int(M/2) + 1)]
    for j in range(0, int(len(cycle)/2)):
        if(cycle[2*j] > cycle[2*j + 1]):
            chr[j] = -chr[j]
    return chr

